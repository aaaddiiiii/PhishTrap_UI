        Developed by three students from Mar Athanasius College of Engineering (MACE), Kothamangalam, PhishTrap is a cutting-edge open-source project designed to combat phishing threats with high accuracy.

🔍 What does it do?
PhishTrap specializes in detecting phishing attempts through:
✔ Email Text Analysis – Identifies malicious intent in email content.
✔ QR Code Inspection – Deciphers and verifies QR codes for hidden threats.
✔ URL Verification – Analyzes suspicious links to detect phishing websites.

🤖 How does it work?
PhishTrap leverages Logistic Regression, a machine learning model trained on multiple large datasets containing real-world phishing and legitimate samples. With advanced feature extraction techniques, it achieves high accuracy in distinguishing phishing from safe content.

